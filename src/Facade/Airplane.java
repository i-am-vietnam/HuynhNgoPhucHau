package Facade;

public class Airplane {
    public void turnOn() {
        System.out.println("Máy bay được bật.");
    }

    public void takeOff() {
        System.out.println("Máy bay cất cánh.");
    }

    public void land() {
        System.out.println("Máy bay hạ cánh.");
    }

    public void turnOff() {
        System.out.println("Máy bay được tắt.");
    }
}
